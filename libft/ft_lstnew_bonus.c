#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	new = (t_list *)malloc(sizeof(*new));
	if (new == NULL)
		return ((void *) 0);
	new->content = content;
	new->next = NULL;
	return (new);
}
